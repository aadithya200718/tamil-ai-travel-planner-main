-- ─── Query History (Legacy Schema) ──────────────────────────────────────────

CREATE TABLE IF NOT EXISTS queries (
  id SERIAL PRIMARY KEY,
  transcript TEXT NOT NULL,
  intent TEXT,
  entities TEXT,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS itineraries (
  id SERIAL PRIMARY KEY,
  query_id INTEGER NOT NULL REFERENCES queries(id) ON DELETE CASCADE,
  itinerary_text TEXT NOT NULL,
  travel_options TEXT,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- ─── Bus Routes ─────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS bus_routes (
  id SERIAL PRIMARY KEY,
  source TEXT NOT NULL,
  destination TEXT NOT NULL,
  bus_type TEXT NOT NULL,
  distance_km INTEGER NOT NULL,
  price_inr INTEGER NOT NULL,
  UNIQUE (source, destination, bus_type, distance_km, price_inr)
);

-- ─── Train Routes ───────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS train_routes (
  id SERIAL PRIMARY KEY,
  source TEXT NOT NULL,
  destination TEXT NOT NULL,
  train_type TEXT NOT NULL,
  train_number TEXT NOT NULL,
  distance_km INTEGER NOT NULL,
  price_inr INTEGER NOT NULL,
  UNIQUE (source, destination, train_type, train_number, distance_km, price_inr)
);

-- ─── Hotels ─────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS hotels (
  id SERIAL PRIMARY KEY,
  name TEXT NOT NULL,
  city TEXT NOT NULL,
  hotel_type TEXT NOT NULL,
  star_rating INTEGER NOT NULL,
  price_per_night INTEGER NOT NULL,
  amenities TEXT,
  description TEXT,
  rating NUMERIC(2,1) DEFAULT 0,
  total_reviews INTEGER DEFAULT 0,
  UNIQUE (name, city)
);

