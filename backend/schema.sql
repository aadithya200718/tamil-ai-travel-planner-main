-- ─── Users ───────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS users (
  id SERIAL PRIMARY KEY,
  name TEXT NOT NULL,
  email TEXT UNIQUE NOT NULL,
  phone TEXT,
  password_hash TEXT NOT NULL,
  preferred_language TEXT DEFAULT 'ta',
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

